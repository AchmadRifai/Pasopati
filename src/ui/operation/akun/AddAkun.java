    private void idKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_idKeyReleased
        if(!id.getText().isEmpty()){
            try {
                java.sql.PreparedStatement ps=d.getPS("select akun from akun where akun=?");ps.setString(1, id.getText());
                java.sql.ResultSet rs=ps.executeQuery();if(rs.next())id.setForeground(Color.red);
                else id.setForeground(Color.BLACK);
                rs.close();ps.close();
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(rootPane, ex.getMessage());
                util.db.hindar(ex);
            }
        }refresh();
    }//GEN-LAST:event_idKeyReleased

