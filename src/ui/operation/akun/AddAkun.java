    private void sActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_sActionPerformed
    try {
        new entity.dao.DAOJejak(d).insert(new entity.Jejak(a.getAkun(), "Menambahkan akun "+id.getText(), java.sql.Date.valueOf(LocalDate.now()),
                java.sql.Time.valueOf(LocalTime.now()),java.net.InetAddress.getLocalHost().getHostAddress()));
        entity.Akun aa=new entity.Akun(id.getText(),pass.getText(),nama.getText(),alamat.getText(),sex.getItemAt(sex.getSelectedIndex())
                , role.isSelected(), false, false, false);
        new entity.dao.DAOAkun(d).insert(aa);
        new entity.dao.DAOJejak(d).insert(new entity.Jejak(aa.getAkun(), "Terdaftar Kerja", java.sql.Date.valueOf(LocalDate.now()),
                java.sql.Time.valueOf(LocalTime.now()),java.net.InetAddress.getLocalHost().getHostAddress()));
        new ui.Admin(d, a).setVisible(true);
        this.setVisible(false);
    } catch (SQLException | UnknownHostException ex) {
        JOptionPane.showMessageDialog(rootPane, ex.getMessage());
        util.db.hindar(ex);
    }
    }//GEN-LAST:event_sActionPerformed

    private void formWindowClosing(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowClosing
        if(!id.getText().isEmpty()||!nama.getText().isEmpty()||!alamat.getText().isEmpty()&&!pass.getText().isEmpty()){
            int x=JOptionPane.showConfirmDialog(rootPane, "Apa anda ingin kembali?", "KEMBALI?", JOptionPane.YES_NO_OPTION);
            if(x==JOptionPane.YES_NO_OPTION){
                try {
                    new entity.dao.DAOJejak(d).insert(new entity.Jejak(a.getAkun(), "Membatalkan Penambahan AKun", java.sql.Date.valueOf(LocalDate.now())
                    ,java.sql.Time.valueOf(LocalTime.now()),java.net.InetAddress.getLocalHost().getHostAddress()));
                    new ui.Admin(d, a).setVisible(true);
                    this.setVisible(false);
                } catch (SQLException | UnknownHostException ex) {
                    JOptionPane.showMessageDialog(rootPane, ex.getMessage());
                    util.db.hindar(ex);
                }
            }else{
                new AddAkun(a,d).setVisible(true);
                this.setVisible(false);
            }
        }else{
            try {
                new entity.dao.DAOJejak(d).insert(new entity.Jejak(a.getAkun(),"Membatalkan penambahan akun",java.sql.Date.valueOf(LocalDate.now()),
                        java.sql.Time.valueOf(LocalTime.now()),java.net.InetAddress.getLocalHost().getHostAddress()));
                new ui.Admin(d, a).setVisible(true);
                this.setVisible(false);
            } catch (SQLException | UnknownHostException ex) {
                JOptionPane.showMessageDialog(rootPane, ex.getMessage());
                util.db.hindar(ex);
            }
        }
    }//GEN-LAST:event_formWindowClosing

