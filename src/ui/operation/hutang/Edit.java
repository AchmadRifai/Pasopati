    private void sActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_sActionPerformed
    try {
        new entity.dao.DAOJejak(d).insert(new entity.Jejak(a.getAkun(), "Menyimpan perubahan hutang "+h.getKode(), java.sql.Date.valueOf(LocalDate.now()),
                java.sql.Time.valueOf(LocalTime.now()),java.net.InetAddress.getLocalHost().getHostAddress()));
        entity.Hutang h2=new entity.Hutang(h.getKode(), d);
        h2.setKe(ke.getText());
        h2.setJum(Long.parseLong(""+jum.getValue()));
        h2.setTgl(tgl.getItemAt(tgl.getSelectedIndex()));
        h2.setAset(aset.getItemAt(aset.getSelectedIndex()));
        h2.setBunga(Float.parseFloat(""+bunga.getValue())/100);
        h2.setAkun(akun.getItemAt(akun.getSelectedIndex()));
        h2.setKet(ket.getText());
        h2.setLunas(lunas.isSelected());
        new entity.dao.DAOHutang(d).update(h, h2);
        if(a.isRole())new ui.Admin(d, a).setVisible(true);
        this.setVisible(false);
    } catch (SQLException | UnknownHostException ex) {
        JOptionPane.showMessageDialog(rootPane, ex.getMessage());
        util.db.hindar(ex);
    }
    }//GEN-LAST:event_sActionPerformed

