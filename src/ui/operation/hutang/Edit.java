    private void refresh() {
        s.setEnabled(!ke.getText().isEmpty()&&!ket.getText().isEmpty()&&!"Pilih Satu :".equals(aset.getSelectedItem()));
    }

    private void ndata() {
    try {
        for(entity.Akun aku:new entity.dao.DAOAkun(d).getData())akun.addItem(aku.getAkun());
        for(entity.Aset as:new entity.dao.DAOAset(d).getData())aset.addItem(as.getKode());
        java.sql.ResultSet rs=d.keluar("select distinct tgl from jejak order by tgl desc");
        while(rs.next())tgl.addItem(rs.getDate("tgl"));rs.close();
    } catch (SQLException ex) {
        JOptionPane.showMessageDialog(rootPane, ex.getMessage());
        util.db.hindar(ex);
    }
    }
